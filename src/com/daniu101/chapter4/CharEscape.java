package com.chapter4;

public class CharEscape {

    public static void main(String[] args) {

        // 定义字符类型
        char ch = 'a';
        System.out.println(ch);

        // 定义转义字符，回车
        char enter = '\r';
        System.out.println(enter);

        char chr = 'b';
        System.out.println(chr);
    }

}
