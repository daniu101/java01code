package com.chapter4;

public class DoubleFloatType {

    public static void main(String[] args) {

        /*
         double占8个字节，是双精度数据类型
         float占4个字节，是单精度数据类型
         */

        // 定义一个双精度数据类型
        double db = 1.123F;
        System.out.println(db);

        // 定义一个单精度数据类型
        float ft = 1.123F;
        System.out.println(ft);

    }

}
