package com.daniu101.chapter6.condition;

public class ConditionIfElseIf {

    public static void main(String[] args) {

        // 定义周几
        int week = 6;

        if (week == 1) {
            System.out.println("今天公司上班！");

        } else if (week == 2) {
            System.out.println("今天公司上班！");

        } else if (week == 3) {
            System.out.println("今天公司上班！");

        } else if (week == 4) {
            System.out.println("今天公司上班！");

        } else if (week == 5) {
            System.out.println("今天公司上班！");

        } else {
            System.out.println("周六日公司放假！");
        }

    }

}
