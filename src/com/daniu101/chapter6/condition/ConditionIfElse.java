package com.daniu101.chapter6.condition;

public class ConditionIfElse {

    public static void main(String[] args) {

        // 定义周几
        int week = 2;

        if (week == 6 || week == 7) {
            System.out.println("今天公司放假！");

        } else {
            System.out.println("今天公司上班！");

        }

    }

}
