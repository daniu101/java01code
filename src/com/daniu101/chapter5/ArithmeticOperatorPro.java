package com.chapter5;

public class ArithmeticOperatorPro {

    public static void main(String[] args) {

        int in = 10;
        char ch = 'A'; // 'A'=65

        // 错误的表示
        // char inch = in + ch;

        // 正确的表示
        int inch = in + ch;
        System.out.println(inch);

    }

}
