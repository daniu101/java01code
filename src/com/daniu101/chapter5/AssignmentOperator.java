package com.daniu101.chapter5;

public class AssignmentOperator {

    public static void main(String[] args) {

        int it = 10;
        int itt = 100;

        // 进行数学算数运算
        int itit = it + itt;
        System.out.println(itit);

        itt += 1;
        System.out.println(itt);

    }

}
