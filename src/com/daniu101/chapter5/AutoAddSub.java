package com.daniu101.chapter5;

public class AutoAddSub {

    public static void main(String[] args) {

        int it = 100;

        // it 在原 100 上，自己增大 1，结果就是101
        it++;
        System.out.println(it);

        int itit = 10;

        // it 在原 10 上，自己减小 1，结果就是9
        itit--;
        System.out.println(itit);

    }


}
